//  Faça um programa que imprima a numeração de 1 à 10. 


for(let i=1; i<=10; i++) {
  console.log(i);
}
// Explicação:
// - O loop `for` inicia em 1 e vai até 10, incrementando `i` em 1 a cada iteração.
// - `console.log(i)` imprime o valor atual de `i` a cada iteração, resultando na numeração de 1 a 10.
// - O loop termina quando `i` ultrapassa 10, garantindo que apenas os números de 1 a 10 sejam impressos.
// - É uma forma simples e eficiente de gerar uma sequência numérica.