//ex003 => Ler um valor e escrever a mensagem É MAIOR QUE 10! se o valor lido for maior 
// que 10, caso contrário escrever NÃO É MAIOR QUE 10! 

const valor = 8 // aqui você pode modar o valor conforme a sua necessidade

if (valor === 10)
    console.log(`${valor} É IGUAL A 10!`)
else if(valor > 10)
    console.log(`${valor} É MAIOR QUE 10!`)

else
    console.log(`${valor} NÃO É MAIOR QUE 10!`)

 